public class Items {
	String Items_name;
	Float Items_unit_price;
	Integer Items_quantity;
	Integer Items_quantity_price;
	public String getItems_name() {
		return Items_name;
	}
	public void setItems_name(String items_name) {
		Items_name = items_name;
	}
	public Float getItems_unit_price() {
		return Items_unit_price;
	}
	public void setItems_unit_price(Float items_unit_price) {
		Items_unit_price = items_unit_price;
	}
	public Integer getItems_quantity() {
		return Items_quantity;
	}
	public void setItems_quantity(Integer items_quantity) {
		Items_quantity = items_quantity;
	}
	public Integer getItems_quantity_price() {
		return Items_quantity_price;
	}
	public void setItems_quantity_price(Integer items_quantity_price) {
		Items_quantity_price = items_quantity_price;
	}
	
}
